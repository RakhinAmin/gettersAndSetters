package gettersandsetters;

public class getterandsetter3 {

  private char alphabet;
  private float digit;
  
  // A method to get the alphabet character - Getter
  public char getAlphabet() {
    return alphabet;
  }
  
  // A method to set the alphabet character - Setter
  public void setAlphabet(char alphabet) {
    this.alphabet = alphabet;
  }
  
  // A method to get the digit - Getter
  public float getDigit() {
    return digit;
  }
  
  // A method to Set the digit - Setter
  public void setDigit(float digit) {
    this.digit = digit;
  }
}
