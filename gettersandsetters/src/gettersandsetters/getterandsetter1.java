package gettersandsetters;

public class getterandsetter1 {

  private String car;
  private double price;
 
  // A method to fetch the car name - getter
  public String getCar() {
    return car;
  }
  
  //A method to set the car name - setter
  public void setCar(String car) {
    this.car = car;
  }

  // A method to fetch the price - getter
  public double getPrice() {
    return price;
  }
  
  //A method to set the price - setter
  public void setPrice (double price) {
    this.price = price;
  }
}
